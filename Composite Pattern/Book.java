    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if(pName.getText().length() == 0 || pSurname.getText().length() == 0 || pEmail.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this, "Yanlis Input!!!");
            return;
        }
        
        StringTokenizer token = new StringTokenizer(pEmail.getText(), "@");
        String address = token.nextToken();
        if(!token.hasMoreTokens())
        {
            JOptionPane.showMessageDialog(this, "Boyle Bir Email Adresi olamaz!!!");
            return;
        }
        
        String temp = pName.getText() + pSurname.getText();
        if(!temp.equalsIgnoreCase(address))
        {
            JOptionPane.showMessageDialog(this, "Gecersiz Email,Name,Surname");
            return;
        }
        
        String name = JOptionPane.showInputDialog("Group Email Address");
        for (int i = 0; i < allEmails.size(); i++) {
            if(allEmails.get(i).getEmail().equals(name)){
                Email e = new PersonalEmail(pName.getText(), pSurname.getText(), pEmail.getText());
                allEmails.get(i).addEmail(e);
                allEmails.add(e);
                pName.setText(null);
                pSurname.setText(null);
                pEmail.setText(null);
                return;
            }
        }
        
        JOptionPane.showMessageDialog(this, "Boyle Bir Grup Yok!!!");
    }//GEN-LAST:event_jButton7ActionPerformed

