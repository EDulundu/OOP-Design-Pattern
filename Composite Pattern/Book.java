    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String e = (String) jList1.getSelectedValue();
        if(e == null)
            return;
        
        StringTokenizer token = new StringTokenizer(e,", ");
        if(token.countTokens() == 2)
        {
            String email = token.nextToken();
            String name = token.nextToken();
            GroupEmail g = new GroupEmail(name, email);
            
            if(allEmails.get(0).equals(g))
            {
                JOptionPane.showMessageDialog(this, "Root'u silemezsiniz!!!");
                return;
            }
            
            for (int i = 0; i < allEmails.size(); i++) {
                allEmails.get(i).removeEmail(g);
                allEmails.remove(g);
                return;   
            }
        }
        else
        {
            String email = token.nextToken();
            String name = token.nextToken();
            String surname = token.nextToken();
            PersonalEmail p = new PersonalEmail(name, surname, email);
           
            for (int i = 0; i < allEmails.size(); i++) {
                allEmails.get(i).removeEmail(p);
                   allEmails.remove(p);
                   return;
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

